// // 1. Melakukan Looping Menggunakan While
console.log('SOAL1');
var loop1 = 0;
console.log('LOOPING PERTAMA');
while (loop1 < 20){
    loop1 += 2;
    console.log(loop1 + ' - I Love coding');
}
    
var loop1a = 22;
console.log('LOOPING KEDUA');
while (loop1a > 2){
    loop1a -= 2;
    console.log(loop1a + ' - I will become fullstack developer');
}    
console.log();

// 2. Melakukan Looping Menggunakan For
console.log('SOAL2');
console.log('LOOPING PERTAMA');
for (var loop2 = 1; loop2 < 21; loop2 +=1){
    console.log(loop2 + ' - I Love coding');
}
    
console.log('LOOPING KEDUA');
for (var loop2a = 20; loop2a > 0; loop2a -=1){
    console.log(loop2a + ' - I will become fullstack developer');
}    
console.log();

